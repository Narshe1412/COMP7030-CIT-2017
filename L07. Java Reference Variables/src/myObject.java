
public class myObject {

	//---------------------------------------
	//	Attributes
	//---------------------------------------
	int value; 
	
	//---------------------------------------
	//	Constructor
	//---------------------------------------
	public myObject(int n){
		this.value = n;
	}
	
	//---------------------------------------
	//	Get Method
	//---------------------------------------	
	public int get_value(){
		return this.value;
	}
	
	//---------------------------------------
	//	Set Method
	//---------------------------------------		
	public void set_value(int n){
		this.value = n;
	}	
		
}
